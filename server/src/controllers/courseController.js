const prisma = require("../utils/prisma");
const { analyzeCourseLink } = require("../services/courseAnalyzer");

exports.addCourse = async (req, res) => {
  try {
    const { title, link, category, description, difficulty, thumbnail } = req.body;

    if (!title || !link || !category) {
      return res.status(400).json({ msg: "Title, link, and category are required" });
    }

    const user = await prisma.user.findUnique({
      where: { id: req.user.userId },
      select: { role: true },
    });

    if (user?.role !== "TEACHER") {
      return res.status(403).json({ msg: "Only teachers can create courses" });
    }

    const course = await prisma.course.create({
      data: {
        title,
        link,
        category,
        description,
        difficulty: difficulty || "BEGINNER",
        thumbnail,
        teacherId: req.user.userId,
      },
    });

    let analysis = { lessons: [], quiz: null, source: "none" };

    try {
      analysis = await analyzeCourseLink({ title, description, category, link });

      if (analysis.lessons?.length) {
        await prisma.courseLesson.createMany({
          data: analysis.lessons.map((lesson) => ({
            courseId: course.id,
            order: lesson.order,
            title: lesson.title,
            videoUrl: lesson.videoUrl,
            duration: lesson.duration,
          })),
        });
      }

      if (analysis.quiz?.questions?.length) {
        await prisma.quiz.create({
          data: {
            title: analysis.quiz.title,
            description: analysis.quiz.description,
            questions: JSON.stringify(analysis.quiz.questions),
            answers: JSON.stringify(analysis.quiz.answers),
            creatorId: req.user.userId,
            courseId: course.id,
            autoGenerated: true,
          },
        });
      }
    } catch (analysisErr) {
      console.log("Course analysis error:", analysisErr);
    }

    const fullCourse = await prisma.course.findUnique({
      where: { id: course.id },
      include: {
        lessons: { orderBy: { order: "asc" } },
        quizzes: { select: { id: true, title: true, description: true, autoGenerated: true } },
      },
    });

    res.status(201).json({
      ...fullCourse,
      analysisSource: analysis.source,
      lessonCount: fullCourse.lessons.length,
    });
  } catch (err) {
    console.log("ERROR:", err);
    res.status(500).json({ msg: err.message });
  }
};

exports.getCourses = async (req, res) => {
  try {
    const { category, difficulty, search, sort } = req.query;

    const filters = {};
    if (category && category !== 'All') filters.category = category;
    if (difficulty && difficulty !== 'All') filters.difficulty = difficulty;

    if (search) {
      filters.OR = [
        { title: { contains: search } },
        { description: { contains: search } },
      ];
    }

    let orderBy = { createdAt: 'desc' };
    if (sort === 'popular') {
      orderBy = { views: 'desc' };
    } else if (sort === 'rating') {
      // Since sorting by dynamic average rating isn't simple in raw MongoDB Prisma queries,
      // we order by views (popularity indicator) as a high-quality fallback.
      orderBy = { views: 'desc' };
    }

    const courses = await prisma.course.findMany({
      where: filters,
      include: {
        teacher: {
          select: {
            id: true,
            name: true,
            username: true,
            avatar: true,
            bio: true,
          }
        },
        _count: {
          select: { enrollments: true, reviews: true }
        }
      },
      orderBy
    });

    res.json(courses);
  } catch (err) {
    console.log("ERROR:", err);
    res.status(500).json({ msg: err.message });
  }
};

exports.getCourseById = async (req, res) => {
  try {
    const { id } = req.params;

    const course = await prisma.course.findUnique({
      where: { id },
      include: {
        teacher: {
          select: {
            id: true,
            name: true,
            username: true,
            avatar: true,
            bio: true,
          },
        },
        lessons: { orderBy: { order: "asc" } },
        quizzes: {
          select: {
            id: true,
            title: true,
            description: true,
            autoGenerated: true,
          },
        },
        reviews: {
          include: {
            student: {
              select: {
                id: true,
                name: true,
                avatar: true,
              },
            },
          },
        },
        enrollments: {
          select: { studentId: true },
        },
      },
    });

    if (!course) {
      return res.status(404).json({ msg: "Course not found" });
    }

    let shouldCountView = false;

    if (req.user?.userId) {
      const viewer = await prisma.user.findUnique({
        where: { id: req.user.userId },
        select: { id: true, role: true },
      });

      const isOwner = course.teacherId === viewer?.id;

      if (!isOwner) {
        const existingView = await prisma.courseView.findUnique({
          where: {
            userId_courseId: {
              userId: req.user.userId,
              courseId: id,
            },
          },
        });

        if (!existingView) {
          await prisma.courseView.create({
            data: { userId: req.user.userId, courseId: id },
          });
          shouldCountView = true;
        }
      }
    }

    if (shouldCountView) {
      await prisma.course.update({
        where: { id },
        data: { views: { increment: 1 } },
      });
      course.views += 1;
    }

    res.json(course);
  } catch (err) {
    console.log("ERROR:", err);
    res.status(500).json({ msg: err.message });
  }
};

