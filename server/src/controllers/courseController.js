exports.getRecommendations = async (req, res) => {
  try {
    const userId = req.user.userId;

    // Find user's enrolled courses to extract interested categories
    const enrollments = await prisma.enrollment.findMany({
      where: { studentId: userId },
      include: { course: true }
    });

    const enrolledCourseIds = enrollments.map(e => e.courseId);
    const interestedCategories = [...new Set(enrollments.map(e => e.course?.category).filter(Boolean))];

    let recommendedCourses = [];

    if (interestedCategories.length > 0) {
      recommendedCourses = await prisma.course.findMany({
        where: {
          category: { in: interestedCategories },
          id: { notIn: enrolledCourseIds }
        },
        include: {
          teacher: {
            select: { id: true, name: true, username: true, avatar: true }
          },
          _count: { select: { enrollments: true, reviews: true } }
        },
        orderBy: [
          { views: 'desc' },
          { createdAt: 'desc' }
        ],
        take: 6
      });
    }

    // Fallback to trending courses if recommendations are few
    if (recommendedCourses.length < 3) {
      const remainingCount = 6 - recommendedCourses.length;
      const recommendedIds = recommendedCourses.map(c => c.id);

      const trending = await prisma.course.findMany({
        where: {
          id: {
            notIn: [...enrolledCourseIds, ...recommendedIds]
          }
        },
        include: {
          teacher: {
            select: { id: true, name: true, username: true, avatar: true }
          },
          _count: { select: { enrollments: true, reviews: true } }
        },
        orderBy: [
          { views: 'desc' }
        ],
        take: remainingCount
      });

      recommendedCourses = [...recommendedCourses, ...trending];
    }

    res.json(recommendedCourses);
  } catch (err) {
    console.log("ERROR:", err);
    res.status(500).json({ msg: err.message });
  }
};

