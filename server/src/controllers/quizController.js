const prisma = require("../utils/prisma");

// Create a quiz
exports.createQuiz = async (req, res) => {
  try {
    const { title, description, questions, answers } = req.body;

    if (!title || !questions || !answers) {
      return res.status(400).json({ msg: "Title, questions, and answers are required" });
    }

    const quiz = await prisma.quiz.create({
      data: {
        title,
        description,
        questions: JSON.stringify(questions),
        answers: JSON.stringify(answers),
        creatorId: req.user.userId
      }
    });

    res.status(201).json(quiz);
  } catch (err) {
    console.log("ERROR:", err);
    res.status(500).json({ msg: err.message });
  }
};

// Get all quizzes
exports.getAllQuizzes = async (req, res) => {
  try {
    const quizzes = await prisma.quiz.findMany({
      include: {
        creator: {
          select: { name: true, username: true }
        }
      },
      orderBy: { createdAt: 'desc' }
    });

    // Parse JSON strings back to arrays
    const parsedQuizzes = quizzes.map(q => ({
      ...q,
      questions: JSON.parse(q.questions)
    }));

    res.json(parsedQuizzes);
  } catch (err) {
    console.log("ERROR:", err);
    res.status(500).json({ msg: err.message });
  }
};

// Get quiz linked to a course (no answers exposed)
exports.getQuizByCourse = async (req, res) => {
  try {
    const { courseId } = req.params;

    const quiz = await prisma.quiz.findFirst({
      where: { courseId },
      orderBy: { createdAt: "desc" },
      include: {
        creator: { select: { name: true, username: true } },
      },
    });

    if (!quiz) {
      return res.status(404).json({ msg: "No quiz for this course" });
    }

    res.json({
      id: quiz.id,
      title: quiz.title,
      description: quiz.description,
      autoGenerated: quiz.autoGenerated,
      courseId: quiz.courseId,
      questions: JSON.parse(quiz.questions),
      creator: quiz.creator,
    });
  } catch (err) {
    console.log("ERROR:", err);
    res.status(500).json({ msg: err.message });
  }
};

