// Submit quiz answers
exports.submitQuiz = async (req, res) => {
  try {
    const { id } = req.params;
    const { userAnswers } = req.body;

    const quiz = await prisma.quiz.findUnique({
      where: { id }
    });

    if (!quiz) {
      return res.status(404).json({ msg: "Quiz not found" });
    }

    const correctAnswers = JSON.parse(quiz.answers);
    let score = 0;

    userAnswers.forEach((answer, index) => {
      if (answer === correctAnswers[index]) {
        score++;
      }
    });

    const result = {
      totalQuestions: correctAnswers.length,
      score,
      percentage: parseFloat(((score / correctAnswers.length) * 100).toFixed(2))
    };

    await prisma.quizAttempt.create({
      data: {
        userId: req.user.userId,
        quizId: id,
        score: result.score,
        totalQuestions: result.totalQuestions,
        percentage: result.percentage
      }
    });

    // Trigger achievement notification and record reward if they got 100%
    if (result.percentage === 100) {
      try {
        await prisma.reward.create({
          data: {
            userId: req.user.userId,
            type: "BADGE",
            badge: `Master of ${quiz.title}`,
            points: 50
          }
        });

        await prisma.notification.create({
          data: {
            userId: req.user.userId,
            message: `Achievement Unlocked! You earned the "Master of ${quiz.title}" badge!`,
            type: "ACHIEVEMENT"
          }
        });
      } catch (err) {
        console.log("Reward/Notification error:", err);
      }
    }

    res.json(result);
  } catch (err) {
    console.log("ERROR:", err);
    res.status(500).json({ msg: err.message });
  }
};

